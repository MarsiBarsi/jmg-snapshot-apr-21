export function fromId(id: string): readonly string[] {
    return Array.from({length: CHARACTERS.length}, (_, i) => i)
        .sort((a, b) => (id.charCodeAt((a + b) % id.length) % 2 === 0 ? 1 : -1))
        .slice(0, 5)
        .map(i => CHARACTERS[i]);
}
